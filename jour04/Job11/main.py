
# Créer la liste d'entiers
L = [7, 11, 42, 39, 2]

# Afficher la liste avant la modification
print("Liste avant la modification :", L)

# Augmenter de 1 la valeur de chaque élément de la liste en utilisant une compréhension de liste
L = [element + 1 for element in L]

# Afficher la liste après la modification
print("Liste après avoir augmenté de 1 chaque élément :", L)


