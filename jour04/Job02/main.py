
def afficher_deuxieme_element():
    fruits = ["pomme", "cerise", "orange"]
    deuxieme_element = fruits[1]  # Les indices en Python commencent à 0, donc l'élément à l'indice 1 est le deuxième élément.
    print(deuxieme_element)

# Appel de la fonction pour afficher le deuxième élément de la liste "fruits"
afficher_deuxieme_element()



