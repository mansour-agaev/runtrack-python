
def determine_dev(langage):
    if langage == "JavaScript":
        print("Tu es un développeur web")
    elif langage == "python":
        print("Tu es un développeur IA")
    elif langage == "java":
        print("Tu es un développeur logiciel")
    elif langage == "reactjs":
        print("Tu es un développeur mobile")
    else:
        print("Un jour, je serai le meilleur développeur...")

# Appels de la fonction determine_dev avec différents paramètres
determine_dev("JavaScript")
determine_dev("python")
determine_dev("java")
determine_dev("reactjs")
determine_dev("C++")
